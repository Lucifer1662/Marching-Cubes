    Vector3 InterpolateVec(int index, float[] edges, int i)
    {
        var vertIndex = TriangleConnectionTable[index, i];
        var oppEdges = edgesFromVert[vertIndex];

     
        float val = edges[oppEdges.x] > edges[oppEdges.y] ? edges[oppEdges.x] : edges[oppEdges.y];
        val = Math.Max(val, 0);
        val = Math.Min(val, 1);
        float flip = edges[oppEdges.x] > edges[oppEdges.y] ? 1 : -1;

        return vertexs[vertIndex] + (interpDir[vertIndex] * (val-0.5f) * flip);
    }




